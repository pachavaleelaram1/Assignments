public class DataTypes {
    public static void main(String[] args) {
        // Integer data type
        int integerVariable = 10;
        System.out.println("Integer Variable: " + integerVariable);

        // Boolean data type
        boolean booleanVariable = true;
        System.out.println("Boolean Variable: " + booleanVariable);

        // Character data type
        char charVariable = 'A';
        System.out.println("Character Variable: " + charVariable);

        // Float data type
        float floatVariable = 10.5f;
        System.out.println("Float Variable: " + floatVariable);

        // Double data type
        double doubleVariable = 20.123456789;
        System.out.println("Double Variable: " + doubleVariable);
    }
}
