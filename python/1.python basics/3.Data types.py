

# Define variables for different data types

# Integer variable
integer_var=15

# Boolean variable (True or False)
boolean_var=True

# Character variable (single character in a string)
char_var='A'

# Float variable (decimal number)
float_var=15.09

'''Note: Python does not have a separate Double data type.
         It uses Float for both single and double precision numbers.'''
float_var_double_precision=98.0123412345678098

# Print variables on the console:

print("Integer:", integer_var)
print("Boolean:", boolean_var)
print("Character:", char_var)
print("Float:", float_var)
print("Float(Double Precision):", float_var_double_precision)
