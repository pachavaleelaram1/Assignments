# 2. Increment and Decrement Operators
'''Increment and decrement operators are used to increase or decrease a variable's value by 1. In Python, we use += and -= operators instead of ++ and --.'''

#Program:

x=5
print("Original value:", x)
x+= 1
print("Incremented value:", x)
x-= 1
print("Decremented value:", x)
