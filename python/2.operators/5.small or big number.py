# 5.Finding Smaller and Larger Numbers
'''We can use if-else statements to find the smaller and larger numbers.'''

#Program:

a = 5
b = 10
if a < b:
    print(a, "is smaller.")
    print(b, "is larger.")
elif a > b:
    print(b, "is smaller.")
    print(a, "is larger.")
else:
    print("Both numbers are equal.")
